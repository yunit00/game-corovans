/**
 * Приводит путь к ассету к базовому пути сборки. В коде ассеты ссылаются
 * абсолютно от корня сайта («/assets/...»), но на хостинге в подкаталоге
 * (например, GitHub Pages: /game-corovans/) корень сайта смещён. Vite
 * подставляет фактическую базу в import.meta.env.BASE_URL (всегда с финальным
 * «/»): на корне домена это «/», в подкаталоге — «/game-corovans/». Все
 * рантайм-загрузчики (модели/текстуры/HDRI/звук) пропускают путь через эту
 * функцию, поэтому одна и та же сборка работает и с корня, и из подкаталога.
 */
export function assetUrl(path: string): string {
  return import.meta.env.BASE_URL + path.replace(/^\//, '');
}
