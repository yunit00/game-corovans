// Скрытый предмет в лесу (волна B): «Старинное ожерелье» висит на стволе одного
// дерева в самой глухой чаще. Какое именно дерево — выбирается ДЕТЕРМИНИРОВАННО:
// ближайшее к фиксированной точке-якорю TREASURE_ANCHOR. Так предмет всегда в
// одном месте при одном лесу, но без маркеров и без «магического» хардкода точки.
//
// Здесь — чистая логика выбора (тестируемая без Three.js). Меш-подвеску и [E]-
// подбор вешает Game, читая позиции сосен из Forest. Подбор — раз за забег, факт
// в сейв (necklaceFound). Нигде в игровых текстах локация не называется.

/**
 * Якорь скрытого предмета: фиксированная точка в густом лесу далеко от всех дорог,
 * деревни (0,120), дворца (0,-380), водопада (-438,300), хижины лесника (-150,250)
 * и сектора форта (+x/+z, [150..350]²). Глубокий -x/-z массив. Сид-независим;
 * cheb 330 — внутри играбельной зоны (мир ±500).
 */
export const TREASURE_ANCHOR = { x: -330, z: -150 } as const;

/** Высота подвески на стволе, м (на уровне глаз — заметна лишь вплотную). */
export const TREASURE_HEIGHT = 1.7;

/** Радиус [E]-подбора ожерелья, м (промпт только вплотную к стволу). */
export const TREASURE_PICK_RADIUS = 2;

/** Дерево-кандидат: достаточно его позиции в плане. */
export interface TreeXZ {
  x: number;
  z: number;
}

/**
 * Выбрать дерево, ближайшее к TREASURE_ANCHOR (детерминированно: один и тот же
 * массив всегда даёт тот же индекс). Возвращает индекс и само дерево, либо null
 * для пустого леса. Сравнение по квадрату дистанции — без sqrt.
 */
export function pickTreasureTree<T extends TreeXZ>(trees: readonly T[]): { index: number; tree: T } | null {
  let bestIdx = -1;
  let bestD2 = Infinity;
  for (let i = 0; i < trees.length; i++) {
    const t = trees[i]!;
    const dx = t.x - TREASURE_ANCHOR.x;
    const dz = t.z - TREASURE_ANCHOR.z;
    const d2 = dx * dx + dz * dz;
    if (d2 < bestD2) {
      bestD2 = d2;
      bestIdx = i;
    }
  }
  if (bestIdx < 0) return null;
  return { index: bestIdx, tree: trees[bestIdx]! };
}
