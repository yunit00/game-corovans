// Чистая логика проекции координат мира в пиксели карты (Фаза 6B волна C).
// Карта рисует квадрат мира WORLD_SIZE×WORLD_SIZE (1 км²) в квадратный canvas
// размера `px`. Только числа — никаких Three/DOM, тестируется в node.
//
// Соглашение осей: ось X мира → ось X карты (вправо), ось Z мира → ось Y карты
// (ВНИЗ). Так север мира (z<0, дворец) оказывается ВВЕРХУ карты, юг (z>0,
// деревня/спавн) — внизу, как на привычной географической карте.

import type { P2 } from './geom';

/** Точка в пикселях карты (начало — левый верхний угол canvas). */
export interface MapPoint {
  /** Пиксель по X (0 — левый край, px — правый). */
  px: number;
  /** Пиксель по Y (0 — верх, px — низ). */
  py: number;
}

/**
 * Проекция мира → пиксели карты. worldSize — длина стороны квадрата мира (м),
 * mapPx — длина стороны canvas карты (px). Центр мира (0,0) попадает в центр
 * карты (mapPx/2, mapPx/2). Возвращает чистую функцию проекции точки.
 */
export class MapProjection {
  readonly worldSize: number;
  readonly mapPx: number;
  /** Пикселей на метр (mapPx / worldSize). */
  readonly scale: number;

  constructor(worldSize: number, mapPx: number) {
    this.worldSize = worldSize;
    this.mapPx = mapPx;
    this.scale = mapPx / worldSize;
  }

  /**
   * Спроецировать точку мира (x по оси X, z по оси Z) в пиксели карты.
   * X мира → X карты (вправо), Z мира → Y карты (вниз). Точки за пределами
   * квадрата мира НЕ обрезаются — вызывающий клампит при необходимости.
   */
  project(x: number, z: number): MapPoint {
    const half = this.worldSize / 2;
    return {
      px: (x + half) * this.scale,
      py: (z + half) * this.scale,
    };
  }

  /** Перевести длину в метрах в пиксели карты (для радиусов прудов/маркеров). */
  lengthToPx(meters: number): number {
    return meters * this.scale;
  }

  /** Спроецировать полилинию (массив P2) в пиксельные точки — для дорог. */
  projectPolyline(pts: readonly P2[]): MapPoint[] {
    const out: MapPoint[] = [];
    for (const p of pts) out.push(this.project(p.x, p.z));
    return out;
  }
}

/**
 * Угол поворота стрелки игрока на карте по yaw камеры/игрока (рад).
 * yaw — поворот вокруг оси Y в мире: 0 смотрит в +Z (юг карты, вниз), растёт
 * против часовой при взгляде сверху. На карте «вверх» = −Z (север). Маркер-
 * стрелка в покое смотрит ВВЕРХ (к северу), поэтому к экранному углу добавляем
 * половину оборота: при yaw=0 (взгляд на юг) стрелка указывает ВНИЗ карты.
 *
 * Возвращает угол в радианах для CSS-rotate (0 — стрелка вверх, по часовой
 * стрелке — вправо, как привязка transform: rotate в DOM).
 */
export function playerArrowAngle(yaw: number): number {
  // yaw=0 → +Z мира → низ карты → стрелка вниз = π (180°).
  // Поворот камеры влево (yaw растёт) при виде сверху крутит направление взгляда
  // против часовой → на карте стрелка должна крутиться по часовой (Z вниз
  // инвертирует знак). Итог: angle = π − yaw.
  return Math.PI - yaw;
}

/** Метка места на карте: позиция в мире, подпись, тип маркера. */
export interface MapMarker {
  x: number;
  z: number;
  /** Короткая подпись места (мелким шрифтом под/над маркером). */
  label: string;
  /** Тип маркера — задаёт дингбат/цвет в отрисовщике. */
  kind: MapMarkerKind;
}

export type MapMarkerKind =
  | 'village' // деревня эльфов (дом)
  | 'palace' // дворец (корона)
  | 'fort' // форт злодея (череп/крест)
  | 'villain_castle' // замок злодея в горах (цитадель — финал)
  | 'inn' // постоялый двор
  | 'forester' // лесничество
  | 'mill' // мельница
  | 'sentry' // сторожевая застава
  | 'waterfall' // водопад
  | 'poi'; // прочие открытые POI (святилище/руины/лагерь)
