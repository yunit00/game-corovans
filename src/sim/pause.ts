// Чистая логика паузы: можно ли ставить/снимать паузу из
// текущего состояния. Без DOM/Three — тестируется в node. Game лишь дёргает эти
// предикаты, держа в одном месте правила «когда Esc ставит паузу, а когда нет».

/** Снимок состояния игры, достаточный для решения о паузе. */
export interface PauseContext {
  /** Игра запущена (меню скрыто, мир активен). До старта паузы нет. */
  started: boolean;
  /** Открыт модальный экран (инвентарь/перки). Esc сначала закрывает его. */
  screenOpen: boolean;
  /** Открыто главное меню (поверх паузы). Из меню паузу не ставят/не снимают. */
  menuOpen: boolean;
  /** Уже на паузе. */
  paused: boolean;
}

/**
 * Можно ли ВОЙТИ в паузу из состояния ctx. Пауза доступна только в активной
 * игре без открытых экранов и меню и когда её ещё нет. Открытый экран I/P
 * перехватывает Esc сам (закрывается), поэтому до паузы дело не доходит.
 */
export function canPause(ctx: PauseContext): boolean {
  return ctx.started && !ctx.screenOpen && !ctx.menuOpen && !ctx.paused;
}

/**
 * Можно ли СНЯТЬ паузу (кнопкой «Продолжить»). Снимаем только если реально на
 * паузе и над ней не висит главное меню (из меню возврат идёт через startGame,
 * который сам снимет паузу).
 */
export function canResume(ctx: PauseContext): boolean {
  return ctx.paused && !ctx.menuOpen;
}
