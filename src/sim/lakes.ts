/** Геометрический центр озера (среднее центров дисков) — для расчистки/карты/смоука. */
export function lakeCenter(lake: LakeSpec): P2 {
  let sx = 0;
  let sz = 0;
  for (const d of lake.discs) {
    sx += d.x;
    sz += d.z;
  }
  return { x: sx / lake.discs.length, z: sz / lake.discs.length };
}

/**
 * Внешний радиус озера от центра: максимум (дист до центра диска + его rimR). Кругом
 * этого радиуса лес/звери исключаются (с запасом), а карта рисует приблизительный
 * след. Чистая функция спецификации.
 */
export function lakeOuterRadius(lake: LakeSpec): number {
  const c = lakeCenter(lake);
  let r = 0;
  for (const d of lake.discs) {
    r = Math.max(r, Math.hypot(d.x - c.x, d.z - c.z) + d.rimR);
  }
  return r;
}

/**
 * Доля «погружения» точки в чашу озера: max по дискам от (1 − smoothstep(flatR,rimR,d)).
 * 1 над ровным дном, плавно к 0 за кромкой. Чистая функция (x,z) и спецификации озера.
 */
function lakeShape(lake: LakeSpec, x: number, z: number): number {
  let s = 0;
  for (const d of lake.discs) {
    const dist = Math.hypot(x - d.x, z - d.z);
    if (dist >= d.rimR) continue;
    const t = 1 - smoothstep(d.flatR, d.rimR, dist);
    if (t > s) s = t;
  }
  return s;
}

/**
 * Врезка чаш всех озёр в рельеф: чистая функция (x,z) и базовой высоты base. Терраин
 * ТОЛЬКО опускается — дно блендится к bedY на долю lakeShape, но не выше базового
 * рельефа (drop ≥ 0). Так чаша садится в любой рельеф без «стен» и не поднимает землю.
 * Возвращает новую высоту. Импортируется Terrain.height() (после холмов, до плато
 * замка — озёра в зонах cheb<400, плато cheb=445, не пересекаются).
 *
 * Дешёвый ранний выход: вне внешнего радиуса всех озёр (+ запас) возвращает base без
 * изменений, поэтому height() вдали от воды не меняется (проверено тестом).
 */
export function carveLakes(x: number, z: number, base: number): number {
  let h = base;
  for (const lake of LAKES) {
    const c = lakeCenter(lake);
    const outer = lakeOuterRadius(lake);
    const dx = x - c.x;
    const dz = z - c.z;
    if (dx * dx + dz * dz > outer * outer) continue; // вне озера — не трогаем
    const s = lakeShape(lake, x, z);
    if (s <= 0) continue;
    // Опускаем дно к bedY на долю s, но не выше текущего рельефа (drop ≥ 0).
    const drop = Math.max(0, h - lake.bedY) * s;
    h -= drop;
  }
  return h;
}

/**
 * Водные диски всех озёр (центр + внешний радиус) — для исключения посадок леса/зверей
 * у воды (как pondDiscs). Буфер берега добавляет вызывающий (inPondWater). Чистая
 * функция спецификации — node-тестируема.
 */
export function lakeWaterDiscs(): WaterDisc[] {
  return LAKES.map((lake) => {
    const c = lakeCenter(lake);
    return { x: c.x, z: c.z, r: lakeOuterRadius(lake) };
  });
}

/**
 * Причал лодки у главного озера west. Детерминированная точка на
 * мелководье у северо-восточной кромки — это сторона ПОДХОДА игрока (деревня/спавн на
 * +z), причал виден с берега. Числа подобраны зондажом РЕАЛЬНОГО рельефа дефолтного
 * мира (seed 42): глубина под лодкой ≈0.5 м (в коридоре 0.3–1 м), до берега ≈2.5 м
 * (≤4 м) — закреплено tests/sim/boat.test.ts с тем же seed (как lakes.test использует
 * свой). waterY берётся из спецификации озера (лодка покоится на глади, не на дне).
 * Курс носа — к центру озера (лодка «смотрит» на воду, отплывать сразу вперёд).
 */
export const BOAT_DOCK = {
  /** id озера, у которого пришвартована лодка (на случай переноса/расширения). */
  lakeId: 'west',
  /** Точка покоя лодки на воде у кромки, мир XZ. */
  x: -238.8,
  z: 50.9,
} as const;

/** Уровень глади озера, где стоит лодка (Y покоя лодки и якорь рендера воды). */
export function boatDockWaterY(): number {
  const lake = LAKES.find((l) => l.id === BOAT_DOCK.lakeId);
  return lake ? lake.waterY : -1.8;
}

/** Курс носа лодки на причале: к центру озера (отплывать вперёд — на воду). */
export function boatDockYaw(): number {
  const lake = LAKES.find((l) => l.id === BOAT_DOCK.lakeId);
  const c = lake ? lakeCenter(lake) : { x: BOAT_DOCK.x, z: BOAT_DOCK.z };
  return Math.atan2(c.x - BOAT_DOCK.x, c.z - BOAT_DOCK.z); // yawFromDir(dx, dz)
}
