// Большие озёра в крупных пустых зонах карты: настоящие озёра, не пруды.
// Это ЧИСТАЯ планировка-
// раскладка (только числа и функции, без three/rapier) — её импортируют и Terrain
// (врезка чаши в height()), и WorldData (расчистка леса в isClear), и рендер-модуль
// world/Lakes.ts (вода/берег). Так одни и те же константы — единственный источник
// истины, без дублирования и без цикла импортов (WorldData уже тянет sim/*).
//
// Идея чаши — тем же паттерном, что холмы (Terrain.HILLS) и плато замка: озеро —
// объединение 2–3 ПЕРЕКРЫВАЮЩИХСЯ дисков (форма не идеальный круг, пригодна для
// лодки). Каждый диск задаёт smoothstep-чашу: внутри flatR — полная глубина
// (ровное дно), к rimR глубина плавно сходит к 0 (пологий вход у берега). Дно
// блендится к bedY (= waterY − глубина), причём терраин только ОПУСКАЕТСЯ (никогда не
// поднимается) — чаша всегда садится в рельеф без «стен». Heightfield-коллайдер
// сэмплирует ту же height(), поэтому игрок заходит в воду по дну (плавания нет).
//
// Места выбраны детерминированно по обзору пустот карты (см. tests/sim/lakes.test.ts):
// каждое озеро ≥45 м от дорог и ≥60 м от деревни/дворца/лагеря/замка/водопада/прудов/
// паркура/POI/концов дорог, озёра не пересекаются между собой/с прудами/озером
// водопада, cheb<400 (внутри играбельной зоны). Сид-независимо — чистые константы.

import { smoothstep } from './noise';
import type { P2 } from './geom';
import type { WaterDisc } from './water';

/** Один диск-составляющая озера: центр, радиус ровного дна и радиус кромки. */
export interface LakeDisc {
  x: number;
  z: number;
  /** Радиус ровного (полная глубина) дна, м. */
  flatR: number;
  /** Радиус кромки воды, м: от flatR до rimR глубина плавно сходит к 0. */
  rimR: number;
}

/** Озеро: уровень воды, дно в центре и набор перекрывающихся дисков-чаш. */
export interface LakeSpec {
  /** Метка для отладки/тестов. */
  id: string;
  /** Уровень водной глади (Y воды), м. Дно чаши лежит ниже на (waterY − bedY). */
  waterY: number;
  /** Высота дна в самой глубокой точке, м (waterY − bedY = глубина центра). */
  bedY: number;
  /** Диски-чаши (объединение даёт неидеально-круглую форму). */
  discs: readonly LakeDisc[];
}

